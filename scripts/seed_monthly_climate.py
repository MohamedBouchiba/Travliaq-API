async def main():
    """Connect to MongoDB and seed monthly_climate data."""
    print(f"Connecting to MongoDB...")
    client = AsyncIOMotorClient(MONGODB_URI)
    db = client[MONGODB_DB]
    collection = db[COLLECTION_NAME]

    updated = 0
    not_found = 0

    for country_code, climate_data in MONTHLY_CLIMATE_DATA.items():
        result = await collection.update_one(
            {"country_code": country_code},
            {"$set": {"monthly_climate": climate_data}},
        )
        if result.matched_count > 0:
            updated += 1
            print(f"  ✅ {country_code}: updated monthly_climate ({len(climate_data)} months)")
        else:
            not_found += 1
            print(f"  ⚠️  {country_code}: profile not found in database")

    print(f"\n{'='*50}")
    print(f"Done! Updated: {updated}, Not found: {not_found}")
    print(f"Total countries in data: {len(MONTHLY_CLIMATE_DATA)}")

    client.close()


if __name__ == "__main__":
    asyncio.run(main())
